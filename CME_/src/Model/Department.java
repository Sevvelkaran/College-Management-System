package Model;

public class Department {

	private int ID;
	private String title;
	public Department() {}
		
		public int getId() {
			return ID;
		}
		
		public void setId(int Id) {
			this.ID = Id;
		}
		
		public String getTitle() {
			return title;
		}
		
		public void setTitle(String title) {
			this.title = title;
		}
	
	

}
